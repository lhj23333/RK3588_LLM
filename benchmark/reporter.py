import os

class MarkdownReporter:
    def __init__(self, workspace_root: str):
        self.output_dir = os.path.join(workspace_root, "results")
        os.makedirs(self.output_dir, exist_ok=True)
        self.report_path = os.path.join(self.output_dir, "benchmark_report.md")
        
    def generate_report(self, results: list):
        with open(self.report_path, "w", encoding="utf-8") as f:
            f.write("# RK3588 LLM/VLM Benchmark Report\n\n")
            
            f.write("## 性能与内存深度剖析 (Performance & Memory Profiling)\n\n")
            f.write("| Model Name | Context Length | Static Model RAM | Dynamic KV-Cache RAM | Total Peak RAM | Generate TPS | Status |\n")
            f.write("| :--- | :--- | :--- | :--- | :--- | :--- | :--- |\n")
            
            for res in results:
                md_mb = res.get('model_data_mb', 0.0)
                kv_mb = res.get('kv_cache_overhead_mb', 0.0)
                tot_mb = res.get('total_peak_mb', 0.0)
                tps = res.get('avg_generate_tps', 0.0)
                
                f.write(f"| {res['model_name']} | {res.get('max_context', 0)} | "
                        f"~{md_mb:.1f} MB | ~{kv_mb:.1f} MB | ~{tot_mb:.1f} MB | "
                        f"{tps:.2f} | {res['status']} |\n")
                
            f.write("\n_Note: Automatically generated by benchmark script. "
                    "Memory values are actively profiled from `/proc/meminfo`._\n")
            
        print(f"Report successfully generated at: {self.report_path}")
