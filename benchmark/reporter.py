import os

class MarkdownReporter:
    def __init__(self, workspace_root: str):
        self.output_dir = os.path.join(workspace_root, "results")
        os.makedirs(self.output_dir, exist_ok=True)
        self.report_path = os.path.join(self.output_dir, "benchmark_report.md")
        
    def generate_report(self, results: list):
        # Format the new rows
        new_rows = []
        for res in results:
            md_mb = res.get('model_data_mb', 0.0)
            kv_mb = res.get('kv_cache_overhead_mb', 0.0)
            tot_mb = res.get('total_peak_mb', 0.0)
            tps = res.get('avg_generate_tps', 0.0)
            npu_core = res.get('npu_core_num', 0)
            
            row = (f"| {res['model_name']} | {res.get('max_context', 0)} | "
                   f"{npu_core} | ~{md_mb:.1f} MB | ~{kv_mb:.1f} MB | ~{tot_mb:.1f} MB | "
                   f"{tps:.2f} | {res['status']} |\n")
            new_rows.append(row)

        if os.path.exists(self.report_path):
            with open(self.report_path, "r", encoding="utf-8") as f:
                lines = f.readlines()
            
            # Find where the table ends
            insert_idx = -1
            for i, line in enumerate(lines):
                if line.startswith("|") and not lines[i+1].startswith("|") if i+1 < len(lines) else True:
                    if line.startswith("|"):
                        insert_idx = i + 1
            
            if insert_idx != -1:
                lines = lines[:insert_idx] + new_rows + lines[insert_idx:]
                with open(self.report_path, "w", encoding="utf-8") as f:
                    f.writelines(lines)
            else:
                # Fallback if no table found
                with open(self.report_path, "a", encoding="utf-8") as f:
                    for row in new_rows:
                        f.write(row)
        else:
            with open(self.report_path, "w", encoding="utf-8") as f:
                f.write("# RK3588 LLM/VLM Benchmark Report\n\n")
                
                f.write("## 性能与内存深度剖析 (Performance & Memory Profiling)\n\n")
                f.write("| Model Name | Context Length | NPU Core | Static Model RAM | Dynamic KV-Cache RAM | Total Peak RAM | Generate TPS | Status |\n")
                f.write("| :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- |\n")
                
                for row in new_rows:
                    f.write(row)
                    
                f.write("\n_Note: Automatically generated by benchmark script. "
                        "Memory values are actively profiled from `/proc/meminfo`._\n")
        
        print(f"Report successfully generated/updated at: {self.report_path}")
