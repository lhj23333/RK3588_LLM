import os
from collections import OrderedDict


class MarkdownReporter:
    def __init__(self, workspace_root: str):
        self.output_dir = os.path.join(workspace_root, "results")
        os.makedirs(self.output_dir, exist_ok=True)
        self.report_path = os.path.join(self.output_dir, "benchmark_report.md")

    def _format_result_row(self, res: dict) -> str:
        md_mb = res.get("model_data_mb", 0.0)
        kv_mb = res.get("kv_cache_overhead_mb", 0.0)
        tot_mb = res.get("total_peak_mb", 0.0)
        cpu_pct = res.get("avg_cpu_usage_percent", 0.0)
        tps = res.get("avg_generate_tps", 0.0)
        npu_core = res.get("npu_core_num", 0)
        return (
            f"| {res['model_name']} | {res.get('max_context', 0)} | "
            f"{npu_core} | ~{md_mb:.1f} MB | ~{kv_mb:.1f} MB | ~{tot_mb:.1f} MB | {cpu_pct:.2f}% | "
            f"{tps:.2f} | {res['status']} |\n"
        )

    def _parse_existing_table_rows(self) -> OrderedDict[str, str]:
        """Read existing report; return model_name -> table row (with newline)."""
        merged: OrderedDict[str, str] = OrderedDict()
        if not os.path.isfile(self.report_path):
            return merged
        try:
            with open(self.report_path, "r", encoding="utf-8") as f:
                lines = f.readlines()
        except OSError:
            return merged

        in_table = False
        for line in lines:
            stripped = line.strip()
            if "| :---" in line or stripped.startswith("|:---"):
                in_table = True
                continue
            if not in_table:
                continue
            if not stripped.startswith("|"):
                break
            parts = [p.strip() for p in stripped.split("|")]
            if len(parts) < 3:
                continue
            model_name = parts[1]
            if not model_name or model_name.lower() == "model name":
                continue
            merged[model_name] = line if line.endswith("\n") else line + "\n"
        return merged

    def generate_report(self, results: list):
        merged_rows = self._parse_existing_table_rows()
        for res in results:
            merged_rows[res["model_name"]] = self._format_result_row(res)

        with open(self.report_path, "w", encoding="utf-8") as f:
            f.write("# RK3588 LLM/VLM Benchmark Report\n\n")
            f.write("## 性能与内存深度剖析 (Performance & Memory Profiling)\n\n")
            f.write(
                "| Model Name | Context Length | NPU Core | Init DRAM (Weights+KV-Cache) | "
                "Runtime Buffer DRAM | Total Peak DRAM (VmHWM) | Avg Runtime CPU Usage | Generate TPS | Status |\n"
            )
            f.write("| :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- |\n")

            for row in merged_rows.values():
                f.write(row)

            f.write(
                "\n_Note: Automatically generated by benchmark script. "
                "Memory values are combined from `/proc/<pid>/status` and RKLLM VmHWM. "
                "CPU usage is (process CPU time / wall time) × 100% during the runtime stage "
                "(multi-threaded loads may exceed 100%). "
                "Re-running a model updates its row; other rows are kept._\n"
            )

        print(f"Report successfully generated/updated at: {self.report_path}")
